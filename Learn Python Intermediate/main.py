# Fundamental Data Types
# int and float
# Arithmetic Operators
print(type(2 + 4))
print(type(2 - 4))
print(type(2 * 4))
print(type(2 / 4))  # 0.5

# exponentiation
print(2 ** 3)  # 2 * 2 * 2 = 8

# floor division
print(5 // 4)  # 1

# modulo operator
print(6 % 4)  # 2

# math functions
print(round(3.9))
print(abs(-20))

# operator precedence
print((20 - 3) + 2 ** 2)


